# A simple fun program asking if you're hungry or not

hungry = input("Hey, you! Are YOU hungry?")

if hungry == "yes":
    print("Here, eat this samosa!")
    print("And... Eat this pizza too!")
    print("Hold up! Eat this doughnut as well!")
else:
    thirsty = input("HEY! Are YOU thirsty?")
    if thirsty == "yes":
        print("DRINK WATER, PLEASE!")
        print("And... Wanna sprite cranberry? DRINK IT!")
